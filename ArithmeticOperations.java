public class ArithmeticOperations {
        public static void main(String[] args) {
            int a = 5;
            int b = 6;
            int c= a + b;
            int d= a - b;
            int e= a * b;

            System.out.println( a + " + " + b + " = " + c);
            System.out.println( a + " - " + b + " = " + d);
            System.out.println( a + " * " + b + " - " + e);

            System.out.println(a);
            System.out.println(a++);
            System.out.println(a);
            System.out.println(++a);
            System.out.println(++a);

        }
    }

